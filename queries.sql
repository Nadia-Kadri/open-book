	id SERIAL PRIMARY KEY,
	title VARCHAR(200) NOT NULL,
	author VARCHAR(100) NOT NULL,
  isbn BIGINT,
	review TEXT,
  rating INT CHECK (rating >= 1 AND rating <= 10),
	date_read DATE
);

